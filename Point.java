public class Point{
    private double x;
    private double y;
    public Point(double ex, double why)
    {
        x = ex;
        y = why;
    }
    
    public double getX()
    {
        return x;
    }
    
    public double getY()
    {
        return y;
    }
    
    public void setX(double ex)
    {
        x = ex;
    }
    
    public void setY(double why)
    {
        y = why;
    }
}
        